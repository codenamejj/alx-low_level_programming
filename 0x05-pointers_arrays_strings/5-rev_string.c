void rev_string(char *s)
{
	int count = 10;
	int index;


	while (s[count])
	{
		
		for (index = 0; index < count; index++)
		{
			if (count >= 0)
				count++;
		}
	}
	while (index)
	{
		for (index = count - 1; index >=0; index --)
			if (count != 0)
				count--;
	}
}
